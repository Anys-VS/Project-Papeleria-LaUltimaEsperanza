    private void jbtn_actualizarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_actualizarProdActionPerformed
        
        int idInsumo = Integer.parseInt(this.jtxt_actProdID.getText());
        String nombre = this.jtxt_actProdNombre.getText();
        int precio = Integer.parseInt(this.jtxt_actProdPrecio.getText());
        int stock = Integer.parseInt(this.jtxt_actProdStock.getText());
        
        Insumo i = new Insumo(nombre, idInsumo, precio, stock,null,null);
        
        Metodo m = new Metodo();
        
        if(m.actualizarInsumo(i)){
            
            JOptionPane.showMessageDialog(this,"Producto Actualizado con Exito!!","Actualizar Producto",1 );
        
        }
        else{
            JOptionPane.showMessageDialog(this,"Producto NO Actualizado","Actualizar Producto",0 );
        
        } 
        
    }//GEN-LAST:event_jbtn_actualizarProdActionPerformed

