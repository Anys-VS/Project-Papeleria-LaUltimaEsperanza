    private void jbtn_buscarIDProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_buscarIDProdActionPerformed
        
        int id = Integer.parseInt(this.jtxt_actProdID.getText());
        
        Metodo m = new Metodo();
        
        Insumo i = m.buscarInsumo(id);
        
        this.jtxt_actProdNombre.setText(i.getNombre());
        this.jtxt_actProdPrecio.setText(String.valueOf(i.getPrecio()));
        this.jtxt_actProdStock.setText(String.valueOf(i.getStock()));
    }//GEN-LAST:event_jbtn_buscarIDProdActionPerformed

