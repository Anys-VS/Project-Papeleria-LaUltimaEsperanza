    private void jbtn_buscarIDEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_buscarIDEmpActionPerformed
        
        int id = Integer.parseInt(this.jtxt_actEmpleadoID.getText());
        
        Metodo m = new Metodo();
        
        Empleado e = m.buscarEmpleado(id);

        this.jtxt_actEmpNombre.setText(e.getNombre());
        this.jtxt_actEmpApPaterno.setText(e.getApPaterno());
        this.jtxt_actEmpRUT.setText(e.getRutEmpleado());
        this.jtxt_actEmpCargo.setText(e.getCargo());
        
    }//GEN-LAST:event_jbtn_buscarIDEmpActionPerformed

