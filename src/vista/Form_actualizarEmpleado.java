    private void jbtn_actualizarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_actualizarEmpActionPerformed
        
        int idEmpleado = Integer.parseInt(this.jtxt_actEmpleadoID.getText());
        String rut = this.jtxt_actEmpRUT.getText();
        String nombre = this.jtxt_actEmpNombre.getText();
        String apPaterno = this.jtxt_actEmpApPaterno.getText();
        String cargo = this.jtxt_actEmpCargo.getText();
        
        Empleado e = new Empleado(idEmpleado, rut, nombre, apPaterno, cargo);
        
        Metodo m = new Metodo();
        
        if(m.actualizarEmpleado(e)){
            
            JOptionPane.showMessageDialog(this,"Empleado Actualizado con Exito!!","Actualizar Empleado",1 );
        
        }
        else{
            JOptionPane.showMessageDialog(this,"Empleado NO Actualizado","Actualizar Empleado",0 );
        
        } 
        
    }//GEN-LAST:event_jbtn_actualizarEmpActionPerformed

