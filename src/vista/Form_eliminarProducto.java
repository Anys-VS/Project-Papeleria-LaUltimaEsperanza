    private void jbtn_eliminarInsumoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_eliminarInsumoActionPerformed

        int idInsumo = Integer.parseInt(this.jtxt_idInsumoEliminar.getText());

        Metodo m = new Metodo();

        int resp = JOptionPane.showConfirmDialog(null, "Â¿Seguro que deseas Eliminar?", "Eliminar Producto", 1);

        if (resp == 0) {

            if (m.eliminarInsumo(idInsumo)) {

                JOptionPane.showMessageDialog(null, "Producto Eliminado con Exito!!", "Eliminar Producto", 1);
            } else {
                JOptionPane.showMessageDialog(null, "Producto NO Eliminado", "Eliminar Producto", 0);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Producto NO Eliminado", "Eliminar Producto", 0);

        }
    }//GEN-LAST:event_jbtn_eliminarInsumoActionPerformed

