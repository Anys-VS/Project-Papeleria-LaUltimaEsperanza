    private void jbtn_eliminarEmpEEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_eliminarEmpEEActionPerformed
        
        int idEmpleado = Integer.parseInt(this.jtxt_eliminarEmpleadoEE.getText());

        Metodo m = new Metodo();

        int resp = JOptionPane.showConfirmDialog(null, "Â¿Seguro que deseas Eliminar?", "Eliminar Empleado", 1);

        if (resp == 0) {

            if (m.eliminarEmpleado(idEmpleado)) {

                JOptionPane.showMessageDialog(null, "Empleado Eliminado con Exito!!", "Eliminar Empleado", 1);
            } else {
                JOptionPane.showMessageDialog(null, "Empleado NO Eliminado", "Eliminar Empleado", 0);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Empleado NO Eliminado", "Eliminar Empleado", 0);

        }
        
    }//GEN-LAST:event_jbtn_eliminarEmpEEActionPerformed

