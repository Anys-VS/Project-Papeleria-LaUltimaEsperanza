    private void jbtn_buscarIDClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_buscarIDClienteActionPerformed

        int id = Integer.parseInt(this.jtxt_idClienteAC.getText());

        Metodo m = new Metodo();

        Cliente c = m.buscarCliente(id);
        
        this.jtxt_rutClienteAC.setText(c.getRutCliente());
        this.jtxt_nombreClienteAC.setText(c.getNombre());
        this.jtxt_apPaternoAC.setText(c.getApPaterno());
        this.jtxt_direccionClienteAC.setText(c.getDireccion());
        this.jtxt_fecNacClienteAC.setText(String.valueOf(c.getFecNac()));
        this.jtxt_telefonoClienteAC.setText(String.valueOf(c.getTelefono()));

    }//GEN-LAST:event_jbtn_buscarIDClienteActionPerformed

