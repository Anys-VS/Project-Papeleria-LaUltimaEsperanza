    private void jbtn_actualizarClienteACActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_actualizarClienteACActionPerformed

        int idCliente = Integer.parseInt(this.jtxt_idClienteAC.getText());
        String rutCliente = this.jtxt_rutClienteAC.getText();
        String nombre = this.jtxt_nombreClienteAC.getText();
        String apPaterno = this.jtxt_apPaternoAC.getText();
        String direccion = this.jtxt_direccionClienteAC.getText();
        Date fecNac = Date.valueOf(this.jtxt_fecNacClienteAC.getText());
        int telefono = Integer.parseInt(this.jtxt_telefonoClienteAC.getText());

        Cliente c = new Cliente(rutCliente, nombre, apPaterno, direccion, fecNac, telefono, idCliente);

        Metodo m = new Metodo();

        if (m.actualizarCliente(c)) {

            JOptionPane.showMessageDialog(this, "Cliente Actualizado con Exito!!", "Actualizar Cliente", 1);

        } else {
            JOptionPane.showMessageDialog(this, "Cliente NO Actualizado", "Actualizar Cliente", 0);

        }

    }//GEN-LAST:event_jbtn_actualizarClienteACActionPerformed

