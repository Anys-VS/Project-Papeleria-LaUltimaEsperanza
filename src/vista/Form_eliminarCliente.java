    private void jbtn_eliminarClienteECActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_eliminarClienteECActionPerformed
    
        int idCliente = Integer.parseInt(this.jtxt_eliminarClienteEC.getText());

        Metodo m = new Metodo();

        int resp = JOptionPane.showConfirmDialog(null, "Â¿Seguro que deseas Eliminar?", "Eliminar Cliente", 1);

        if (resp == 0) {

            if (m.eliminarCliente(idCliente)) {

                JOptionPane.showMessageDialog(null, "Cliente Eliminado con Exito!!", "Eliminar Cliente", 1);
            } else {
                JOptionPane.showMessageDialog(null, "Cliente NO Eliminado", "Eliminar Cliente", 0);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Cliente NO Eliminado", "Eliminar Cliente", 0);

        }
        
    }//GEN-LAST:event_jbtn_eliminarClienteECActionPerformed

